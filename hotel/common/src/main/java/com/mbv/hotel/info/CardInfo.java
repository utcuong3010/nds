package com.mbv.hotel.info;

public class CardInfo {
	
	private String typeCard;
	private String cardNumber;
	private String cardHotelName;
	private String expiryDate;
	private String cvcCode;
	private String countryOfIssuingBank;
	private String bankGaveCard;
	
	
	public String getTypeCard() {
		return typeCard;
	}
	public void setTypeCard(String typeCard) {
		this.typeCard = typeCard;
	}
	public String getCardNumber() {
		return cardNumber;
	}
	public void setCardNumber(String cardNumber) {
		this.cardNumber = cardNumber;
	}
	public String getCardHotelName() {
		return cardHotelName;
	}
	public void setCardHotelName(String cardHotelName) {
		this.cardHotelName = cardHotelName;
	}
	public String getExpiryDate() {
		return expiryDate;
	}
	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}
	public String getCvcCode() {
		return cvcCode;
	}
	public void setCvcCode(String cvcCode) {
		this.cvcCode = cvcCode;
	}
	public String getCountryOfIssuingBank() {
		return countryOfIssuingBank;
	}
	public void setCountryOfIssuingBank(String countryOfIssuingBank) {
		this.countryOfIssuingBank = countryOfIssuingBank;
	}
	public String getBankGaveCard() {
		return bankGaveCard;
	}
	public void setBankGaveCard(String bankGaveCard) {
		this.bankGaveCard = bankGaveCard;
	}
	
	
}
