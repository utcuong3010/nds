package com.mbv.frontend.model;

public class LockTelcoInfoModel {
	private String telcoId;
	private String lockAmount;
	private String providerId;
	private String providerAmount;
	private String providerIds;
	private String providerAmountSumary;
	public String getTelcoId() {
		return telcoId;
	}
	public void setTelcoId(String telcoId) {
		this.telcoId = telcoId;
	}
	public String getLockAmount() {
		return lockAmount;
	}
	public void setLockAmount(String lockAmount) {
		this.lockAmount = lockAmount;
	}
	public String getProviderId() {
		return providerId;
	}
	public void setProviderId(String providerId) {
		this.providerId = providerId;
	}
	public String getProviderAmount() {
		return providerAmount;
	}
	public void setProviderAmount(String providerAmount) {
		this.providerAmount = providerAmount;
	}
	public String getProviderIds() {
		return providerIds;
	}
	public void setProviderIds(String providerIds) {
		this.providerIds = providerIds;
	}
	public String getProviderAmountSumary() {
		return providerAmountSumary;
	}
	public void setProviderAmountSumary(String providerAmountSumary) {
		this.providerAmountSumary = providerAmountSumary;
	}
	
}
